CREATE TABLE EMAIL (
    ID int(11) NOT NULL auto_increment,
    USEREMAIL varchar(50) NOT NULL,
    FROMEMAIL varchar(50) NOT NULL,
    SUBJECT varchar(50) NOT NULL,
    SENDDATE datetime,
    RECEIVEDATE datetime,
    FOLDERNAME varchar(40) NOT NULL,
    FLAGS varchar(40),
    PRIMARY KEY (ID)
);


